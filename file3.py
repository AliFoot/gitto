# 1. Defining the function
def greet_friends(person1, person2, person3):
    print(f"Hello {person1}!")
    print(f"Hey there {person2}!")
    print(f"Hi {person3}!")


# 2. Assigning multiple variables on one line
x, y, z = "Jack", "Alice", "Bob"


# 3. Calling the function using our variables
greet_friends(x, y, z)
